package com.java.perametr_argument;

//Parameters are the variables that are declared in the method signature. 
//They act as placeholders for the values that the method will receive when it is called.
//For example, in this method:

public class parameter {
	public static int add(int x, int y) { // Parametr
		return x + y;
	}

	public static void main(String[] args) {
		add(10,20);  // Argument.
	}
}
