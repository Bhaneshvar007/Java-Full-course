//Arithmetic operatore is : + , - , * , / , %
// Arithmetic operators are used to perform common mathematical operations.

package com.java.operatore;

public class Arithmetic {
	public static void main(String[] args) {
		
 		int a = 100,b=200;
		int c = a+b;
		System.out.println("The sum of " + a + " & "+ b +" is : " + c);
		
		int a1 = 100,b1=50;
		int c1 = a1-b1;
		System.out.println("The Subtraction of " + a1 + " & "+ b1 +" is : " + c1);
		
		int a2 = 100,b2=200;
		int c2 = a2*b2;
		System.out.println("The Multiplication of " + a2 + " & "+ b2 +" is : " + c2);
		
		int a3 = 10,b3=2;
		int c3 = a3/b3;
		System.out.println("The Division of " + a3 + " & "+ b3 +" is : " + c3);
		
	}
}
